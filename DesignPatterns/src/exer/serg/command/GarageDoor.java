package exer.serg.command;

public class GarageDoor {
	
	public void up() {
		System.out.println("Door goes up!");
	}
	
	public void down() {
		System.out.println("Door goes down!");
	}
	
	public void stop() {
		System.out.println("Door is stopped!");
	}
	
	public void lightOn() {
		System.out.println("Garage door light is on!");
	}
	
	public void lightOff() {
		System.out.println("Garage door light is off!");
	}
}
